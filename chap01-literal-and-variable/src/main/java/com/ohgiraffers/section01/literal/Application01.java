package com.ohgiraffers.section01.literal;

public class Application01 { // 스코프

    public static void main(String[] args) {

        // System. ( . = 참조연산자) out = out과 연관된 것들 println()  ( () = 메서드 )

        System.out.println(123); // 정수형 리터럴 :(코드해석) 시스템패키지 안을 참조해서 아웃안에 들어가서 매서드 안의 내용을 한 줄 뛰기하여 출력하라.

        System.out.println(1.1); // 실수형 리터럴

        System.out.println('A'); // 문자형 리터럴

        System.out.println("Hello World");  // 문자열형 리터럴
        System.out.println("12345");
        System.out.println("123.45");

        System.out.println(true); //논리형 리터럴
        System.out.println(false);

        //리터럴이란 값 자체를 의미한다.
    }
}
